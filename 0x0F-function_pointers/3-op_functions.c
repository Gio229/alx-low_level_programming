#include "3-calc.h"

/**
 * op_add - sum two integer
 * @a: int
 * @b: int
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - get difference of two integer
 * @a: int
 * @b: int
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two integer
 * @a: int
 * @b: int
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two integer
 * @a: int
 * @b: int
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds modulus of two integer
 * @a: int
 * @b: int
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
